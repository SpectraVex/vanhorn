// Core dependencies
#include <stdio.h>

// External dependencies
#include <raylib.h>

// Vanhorn headers
#include "vanhorn/core/vhcore.h"

//------------------------------------------------------------------------------------
// Main entry point
//------------------------------------------------------------------------------------
int main(void) {
    // Core Initialization
    //--------------------------------------------------------------------------------------
    const int screenWidth = 640;
    const int screenHeight = 480;

    InitWindow(screenWidth, screenHeight, "Vanhorn - Build v1.0");
    InitAudioDevice();
    //--------------------------------------------------------------------------------------

    // Resource Initialization
    //--------------------------------------------------------------------------------------
    Music music = LoadMusicStream("D:/Studio/Games/vanhorn/res/snd/amb_loop_00.wav"); 
    PlayMusicStream(music);

    SetTargetFPS(244);
    //--------------------------------------------------------------------------------------

    // Main game loop
    while (!WindowShouldClose()) {
        // Update input
        //----------------------------------------------------------------------------------
        //UpdateInput();
        //----------------------------------------------------------------------------------

        // Update game
        //----------------------------------------------------------------------------------
        //UpdateGame();
        //----------------------------------------------------------------------------------

        // Update audio 
        //----------------------------------------------------------------------------------
        UpdateMusicStream(music);
        //----------------------------------------------------------------------------------

        // Update rendering
        //----------------------------------------------------------------------------------
        BeginDrawing();
            ClearBackground(BLACK);
            DrawText("Welcome to Vanhorn...", 210, 210, 20, DARKGRAY);
        EndDrawing();
        //----------------------------------------------------------------------------------
    }


    // De-Initialization
    //--------------------------------------------------------------------------------------
    //UnloadResources();
    UnloadMusicStream(music);

    // Core De-Initialization
    //--------------------------------------------------------------------------------------
    CloseAudioDevice();
    CloseWindow();
    //--------------------------------------------------------------------------------------

    return 0;
}